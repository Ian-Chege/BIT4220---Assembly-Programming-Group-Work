; ============================================================
; control.asm — Task 4: Control Structures Translator
; BIT 4220 Assembly Programming | x86-64 Linux
; ============================================================
;
; Shows how five common high-level control constructs map onto
; x86-64 CMP/JMP instructions.  The program is themed as a
; prepaid utility-meter control module.
;
;   Demo 1 — IF-ELSE   : cascaded comparisons + conditional jumps
;   Demo 2 — WHILE     : condition check at the TOP  (may run 0 times)
;   Demo 3 — DO-WHILE  : condition check at the BOTTOM (runs ≥ 1 time)
;   Demo 4 — FOR       : counter-controlled loop with known bounds
;   Demo 5 — SWITCH    : O(1) jump-table dispatch (as generated by compilers)
;
; BUILD
;   nasm -f elf64 control.asm -o control.o && ld control.o -o control
;   Or:  ./run.sh
; ============================================================

